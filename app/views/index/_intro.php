<section>
    <h2 class="section-title">What is Unda?</h2>
    <p>
        Unda is an online video sharing site that maintains focus on gaining exposure for videos. Every
        day in the comments section of popular video streaming sites, small channels will advertise themselves
        in an attempt to expand their audience. Unda removes that.
    </p>
    <p>
        Based in Brisbane, Australia, Unda started with the mission to help content creators to
        achieve notority for their content without having to hire a publisher, pay
        for expensive marketing, or spending hours every week on social media sites like Facebook
        and Reddit.
    </p>
</section>
